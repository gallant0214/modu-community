export async function POST(request: Request) {
  const body = await request.json().catch(() => ({} as { password?: string; cacheOnly?: boolean; debug?: boolean }));
  const password = body.password ?? "";
  const cacheOnly = body.cacheOnly === true;
  const debug = body.debug === true;

  // 디버그 모드: 평문 비교 없이 길이/source만 반환 (안전)
  if (debug) {
    const { data: dbRow } = await supabase
      .from("admin_settings")
      .select("value")
      .eq("key", "admin_password")
      .maybeSingle();
    const dbVal = dbRow?.value ?? null;
    const envVal = process.env.ADMIN_PASSWORD ?? null;
    return NextResponse.json({
      debug: true,
      input_length: password.length,
      input_first_charcode: password.length > 0 ? password.charCodeAt(0) : null,
      input_last_charcode: password.length > 0 ? password.charCodeAt(password.length - 1) : null,
      db_set: dbVal !== null,
      db_length: dbVal?.length ?? null,
      env_set: envVal !== null,
      env_length: envVal?.length ?? null,
      match_db: dbVal !== null && password === dbVal,
      match_env: envVal !== null && password === envVal,
      effective_source: dbVal !== null ? "db" : (envVal !== null ? "env" : "none"),
    });
  }

  if (!(await verifyAdminPassword(password))) {
    return NextResponse.json({ error: "관리자 비밀번호가 일치하지 않습니다" }, { status: 403 });
  }

  const allCategoryIds = Array.from(new Set(newPosts.map((p) => p.categoryId)));

  if (cacheOnly) {
    await flushCommunityCache(allCategoryIds);
    return NextResponse.json({ success: true, cacheOnly: true, revalidated: ["/", "/community", ...allCategoryIds.map((id) => `/category/${id}`)] });
  }

  let postsInserted = 0;
  let commentsInserted = 0;

  for (const post of newPosts) {
    const region = pickRegion();
    const views = Math.floor(Math.random() * 81) + 20;

    const { data: insertedPost, error: postErr } = await supabase
      .from("posts")
      .insert({
        category_id: post.categoryId,
        title: post.title,
        content: post.content,
        author: post.author,
        password: "__seed_community__",
        region,
        tags: "기타",
        ip_address: "seed_community",
        created_at: post.date,
        updated_at: post.date,
        views,
      })
      .select("id")
      .single();

    if (postErr || !insertedPost) {
      continue;
    }
    postsInserted++;
    const postId = insertedPost.id;

    for (const c of post.comments) {
      const cd = new Date(post.date);
      cd.setHours(cd.getHours() + Math.floor(Math.random() * 48) + 1);
      cd.setMinutes(Math.floor(Math.random() * 60));

      const { error: cErr } = await supabase.from("comments").insert({
        post_id: postId,
        author: c.author,
        password: "__seed_community__",
        content: c.content,
        ip_address: "seed_community",
        created_at: cd.toISOString(),
      });
      if (!cErr) commentsInserted++;
    }

    const { count } = await supabase
      .from("comments")
      .select("*", { count: "exact", head: true })
      .eq("post_id", postId);
    await supabase
      .from("posts")
      .update({ comments_count: count ?? 0 })
      .eq("id", postId);
  }

  await flushCommunityCache(allCategoryIds);

  return NextResponse.json({ success: true, postsInserted, commentsInserted });
}
